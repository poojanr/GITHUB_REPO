package uk.gov.nhs.digital.ers.service.domain;

import java.util.ArrayList;
import java.util.List;

public class ReferralStatusLookupDTO {

	private Integer id;
	private String status;
	private String description;
	private List<ReferralRequestDTO> referralRequests;

	public ReferralStatusLookupDTO() {
		referralRequests = new ArrayList<ReferralRequestDTO>();
	}

	public ReferralStatusLookupDTO(Integer id, String status, String description) {
		this();
		this.id = id;
		this.status = status;
		this.description = description;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public List<ReferralRequestDTO> getReferralRequests() {
		return referralRequests;
	}

	public void setReferralRequests(List<ReferralRequestDTO> referralRequests) {
		this.referralRequests = referralRequests;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ReferralStatusLookupDTO other = (ReferralStatusLookupDTO) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}
}